package com.bitcamp.dto;

public class TourMarkerDTO {
	private Integer tourno;
	private Integer spotno;
	private String spottitle;
	private String spotcontent;
	private String spotlat;
	private String spotlng;
	
	public Integer getTourno() {
		return tourno;
	}
	public void setTourno(Integer tourno) {
		this.tourno = tourno;
	}
	public Integer getSpotno() {
		return spotno;
	}
	public void setSpotno(Integer spotno) {
		this.spotno = spotno;
	}
	public String getSpottitle() {
		return spottitle;
	}
	public void setSpottitle(String spottitle) {
		this.spottitle = spottitle;
	}
	public String getSpotcontent() {
		return spotcontent;
	}
	public void setSpotcontent(String spotcontent) {
		this.spotcontent = spotcontent;
	}
	public String getSpotlat() {
		return spotlat;
	}
	public void setSpotlat(String spotlat) {
		this.spotlat = spotlat;
	}
	public String getSpotlng() {
		return spotlng;
	}
	public void setSpotlng(String spotlng) {
		this.spotlng = spotlng;
	}
	
	
		
}
